package com.aryasindhu.queue.exception;

/**
 * 
 * @author Aryasindhu Sahu
 *
 */
public class InvalidCapabilityException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -6083371085970540201L;

	public InvalidCapabilityException() {
	}

	public InvalidCapabilityException(String arg0) {
		super(arg0);
	}

	public InvalidCapabilityException(Throwable arg0) {
		super(arg0);
	}

	public InvalidCapabilityException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public InvalidCapabilityException(String arg0, Throwable arg1,
			boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
